/**
 * Get exercises by category
 */
export function getExercisesByCategory(categoria: ExerciseInfo['categoria']): ExerciseInfo[] {
  return EXERCISE_DATABASE.filter(ex => ex.categoria === categoria);
}

/**
 * Get exercises by modality
 */
export function getExercisesByModality(modalidade: string): ExerciseInfo[] {
  return EXERCISE_DATABASE.filter(ex => ex.modalidade.includes(modalidade));
}

/**
 * Get exercises by muscle group
 */
export function getExercisesByMuscleGroup(muscleGroup: string): ExerciseInfo[] {
  const lowerGroup = muscleGroup.toLowerCase();
  return EXERCISE_DATABASE.filter(ex =>
    ex.gruposMusculares.principal.some(m => m.toLowerCase().includes(lowerGroup)) ||
    ex.gruposMusculares.secundario.some(m => m.toLowerCase().includes(lowerGroup))
  );
}

/**
 * Get exercise by ID
 */
export function getExerciseById(id: string): ExerciseInfo | undefined {
  return EXERCISE_DATABASE.find(ex => ex.id === id);
}

/**
 * Get exercise by name (partial match)
 */
export function getExerciseByName(nome: string): ExerciseInfo | undefined {
  const lowerNome = nome.toLowerCase();
  return EXERCISE_DATABASE.find(ex =>
    ex.nome.toLowerCase().includes(lowerNome) ||
    ex.nomeIngles.toLowerCase().includes(lowerNome)
  );
}

/**
 * Search exercises
 */
export function searchExercises(query: string): ExerciseInfo[] {
  const lowerQuery = query.toLowerCase();
  return EXERCISE_DATABASE.filter(ex =>
    ex.nome.toLowerCase().includes(lowerQuery) ||
    ex.nomeIngles.toLowerCase().includes(lowerQuery) ||
    ex.categoria.toLowerCase().includes(lowerQuery) ||
    ex.modalidade.some(m => m.toLowerCase().includes(lowerQuery)) ||
    ex.gruposMusculares.principal.some(m => m.toLowerCase().includes(lowerQuery)) ||
    ex.gruposMusculares.secundario.some(m => m.toLowerCase().includes(lowerQuery))
  );
}
